/**
 * Defines the substitutoo shift scheduler entity class.
 *
 * @ContentEntityType(
 *   id = "sub_shift_scheduler",
 *   label = @Translation("Service Contract"),
 *   label_collection = @Translation("Service Contracts"),
 *   label_singular = @Translation("Service Contract"),
 *   label_plural = @Translation("substitutoo shift schedulers"),
 *   label_count = @PluralTranslation(
 *     singular = "@count substitutoo shift schedulers",
 *     plural = "@count substitutoo shift schedulers",
 *   ),
 *   handlers = {
 *     "access" = "Drupal\entity\EntityAccessControlHandler",
 *     "query_access" = "Drupal\entity\QueryAccess\QueryAccessHandler",
 *     "permission_provider" = "Drupal\entity\EntityPermissionProvider",
 *     "view_builder" = "Drupal\Core\Entity\EntityViewBuilder",
 *     "list_builder" = "Drupal\sub_shift_scheduler\SubstitutooShiftSchedulerListBuilder",
 *     "views_data" = "Drupal\views\EntityViewsData",
 *     "permission_provider" = "Drupal\entity\EntityPermissionProvider",
 *     "form" = {
 *       "add" = "Drupal\sub_shift_scheduler\Form\SubstitutooShiftSchedulerForm",
 *       "edit" = "Drupal\sub_shift_scheduler\Form\SubstitutooShiftSchedulerForm",
 *       "delete" = "Drupal\Core\Entity\ContentEntityDeleteForm",
